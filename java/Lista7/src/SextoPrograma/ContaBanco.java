package SextoPrograma;

public class ContaBanco {
	public String banco;
	public String nome;
	public String cpf;
	public double conta;
	public double agencia;
	public double saldo;
	
	public ContaBanco(String banco, String nome, String cpf, double conta, double agencia, double saldo) {
		super();
		this.banco=banco;
		this.nome = nome;
		this.cpf = cpf;
		this.conta = conta;
		this.agencia = agencia;
		this.saldo = saldo;
	}

	
}
