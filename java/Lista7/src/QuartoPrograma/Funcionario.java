package QuartoPrograma;

public class Funcionario {
	public String nome;
	public int codigo;
	public int idade;
	public String setor;
	
	
	public Funcionario(String nome, int codigo, int idade, String setor) {
		super();
		this.nome = nome;
		this.codigo = codigo;
		this.idade = idade;
		this.setor = setor;
	}
	
	
	

}
