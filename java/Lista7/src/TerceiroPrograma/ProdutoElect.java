package TerceiroPrograma;

public class ProdutoElect {
	
	public String componenteA;
	public String componenteB;
	public double valor;
	public String tamanho;
	
	public ProdutoElect(String componenteA, String componenteB, double valor, String tamanho) {
		super();
		this.componenteA = componenteA;
		this.componenteB = componenteB;
		this.valor = valor;
		this.tamanho = tamanho;
	}
	
	

}
