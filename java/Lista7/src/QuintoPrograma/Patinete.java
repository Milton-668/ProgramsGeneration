package QuintoPrograma;

public class Patinete {

	public String tamanho;
	public double preco;
	public String cor;
	public int codigo;
	
	public Patinete(String tamanho, double preco, String cor, int codigo) {
		super();
		this.tamanho = tamanho;
		this.preco = preco;
		this.cor = cor;
		this.codigo = codigo;
	}
	
	
}
